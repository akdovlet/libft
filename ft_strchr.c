#include "libft.h"

char	*strchr(const char *s, int c)
{
	int		i;
	char 	*str;

	str = (char *)s;
	i = 0;
	while (str[i] != (char)c)
	{
		if (str[i] == '\0')
			return (0);
		i++;
	}
	return (str + i);	
}
